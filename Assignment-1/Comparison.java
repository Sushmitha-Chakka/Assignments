class Comparison {
	public static void main(String []args){
		int a=10;
		int b=20;
		int c=30;
		int d=5;
		System.out.println("Equal to result : "+(a==b));
		System.out.println("Not Equal to result : "+(c!=b));
		System.out.println(" Greater than result : "+(c>d));
		System.out.println("Lesser than result : "+(a<d));
		System.out.println("Greater than or Equal to result : "+(b>=a));
		System.out.println("Lesser than or Equal to result : "+(d<=c));
	}
}